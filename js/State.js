/**
 * STATE.js — Мозг системы (БЕТОН)
 * Роль: Управление этапами жизни проекта и режимами взаимодействия.
 */

window.State = {
    // Начальная точка — инициализация систем
    current: 'INIT', 

    /**
     * Возможные состояния (для навигации):
     * INIT        — Загрузка файлов и подготовка базы.
     * LOADING     — Оцифровка букв и создание "золотого запаса".
     * READY       — Буквы замерли. Можно смотреть и трогать пустоту (красота в покое).
     * DRAGGING    — Захват золота. Палец на букве, песчинки летят за ним.
     * RESTORE     — Палец убран. Золото возвращается в контуры букв.
     */

    /**
     * set — Единственный способ переключить режим проекта.
     * @param {string} newState — Новое состояние из списка выше.
     */
    set(newState) {
        // Защита: не переключаем, если состояние уже активно
        if (this.current === newState) return;

        const oldState = this.current;
        this.current = newState;

        // Яркая вспышка в консоли для контроля переключений
        if (window.Logger) {
            Logger.log('STATE', `${oldState} —> ${newState}`, '🧠');
        }
    }
};

// --- АТОМАРНЫЙ СТАНДАРТ ЗАПУСКА ---

const t0_state = performance.now();

// Мы не используем Object.freeze для всего объекта, так как 'current' должен меняться.
// Но мы можем заморозить саму структуру методов, если захотим (уровень PRO).

const duration_state = (performance.now() - t0_state).toFixed(4);

// Финальный рапорт в консоль
if (window.Logger) {
    Logger.log('SYSTEM', `Мозг (State) настроен под интерактив за ${duration_state} мс`, '🧠');
}