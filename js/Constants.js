/**
 * CONSTANTS.js — Законы физики и математики
 * Здесь мы фиксируем базовые числа, на которые будут опираться все модули.
 */

    window.CONSTANTS = {
    // 1. МАТЕМАТИЧЕСКИЕ КОНСТАНТЫ
    // Используются для расчета кругового движения каждой песчинки (песочные часы).
    PI: Math.PI,             // 3.14... — для расчетов полуокружности
    TWO_PI: Math.PI * 2,     // 6.28... — для полного круга (360 градусов)
    HALF_PI: Math.PI / 2,    // 1.57... — для четверти круга (90 градусов)

    // 2. СИСТЕМНЫЕ ЛИМИТЫ (Защита от перегрузки)
    // Ограничиваем общее количество песчинок, чтобы проект летал даже на смартфонах.
    MAX_PARTICLES: 100000,  // Один миллион частиц — предел для "ювелирной" плотности

    // 3. ФИЗИЧЕСКИЕ КОНСТАНТЫ
    // Базовые множители, которые определяют "густоту" времени и пространства.
    TARGET_FPS: 60,          // Целевая скорость (кадры в секунду) для плавности песка
    TIME_STEP: 1 / 60,       // Шаг времени между кадрами (примерно 0.016 сек)
    
    // 4. ГРАФИЧЕСКИЕ КОНСТАНТЫ
    // Параметры для WebGL, отвечающие за восприятие глубины Бездны.
    WORLD_SCALE: 1.0,        // Глобальный масштаб букв (1.0 — оригинальный размер)
    Z_NEAR: 0.1,             // Ближняя граница видимости
    Z_FAR: 2000.0,           // Дальняя граница видимости в глубину Бездны

    // 5. КОЭФФИЦИЕНТЫ ПЕРЕСЧЕТА
    // Нужны для перевода человеческих градусов в радианы, понятные видеокарте.
    DEG_TO_RAD: Math.PI / 180,
    RAD_TO_DEG: 180 / Math.PI
};

const t0 = performance.now(); // <--- СТАВЬ СЮДА 

/**
 * Object.freeze — критически важная команда.
 * Она "бетонирует" объект, запрещая любому коду изменять эти константы.
 * Это гарантирует стабильность всей системы файлов.
 */
Object.freeze(window.CONSTANTS);

const duration = (performance.now() - t0).toFixed(4); 
Logger.log('SYSTEM', `Законы мира (Constants) установлены за ${duration} мс`, '📐'); 
