/**
 * SHADERSSOURCE.js — Тотальное Спасение Воздуха и Чистый Рендеринг Ноутбука ПК (БЕТОН)
 */

window.ShadersSource = {

    // ВЕРШИННЫЙ ШЕЙДЕР: Абсолютный покой каллиграфии
    vertex: `
        precision highp float;

        attribute vec2 a_pos;    
        attribute vec2 a_home;   
        attribute float a_id;    
        attribute float a_size;  

        uniform vec2 u_res;
        uniform vec2 u_mouse; // --- ГЛАВНЫЙ СИСТЕМНЫЙ КАБЕЛЬ ЖИВЫХ КООРДИНАТ ПАЛЬЦА/МЫШКИ ---
        uniform float u_speed; // --- ЖИВОЙ ТОК СКОРОСТИ ДВИЖЕНИЯ РУКИ ИЗ UNIFORMS.JS (ВСТАВКА) ---
        uniform float u_dpr;
        
        varying float v_id; // --- СКВОЗНОЙ КАБЕЛЬ ПЕРЕДАЧИ ID ПЕСЧИНКИ В ЭКРАН ---

        void main() {
            v_id = a_id; // Запечатываем паспортный номер точки в кабель

            // Монументальный покой! Буквы держат свою изящную форму.
            vec2 pos = a_home; 

            // Перевод пикселей Завода в пространство WebGL NDC (Центр экрана равен 0.0)
            vec2 ndcPos = (pos / u_res) * 2.0 - 1.0;

            // 1. Приводим ось Y провода мыши u_mouse к законам WebGL холста
            vec2 targetMouse = vec2(u_mouse.x, -u_mouse.y);

            // 2. Рассчитываем вектор отталкивания ОТ пальца к песчинке и точную длину этой дистанции
            vec2 waveDir = ndcPos - targetMouse;
            float waveDist = length(waveDir);

            // 3. СИЛА ВОЛНЫ БЕЗДНЫ (РЫЧАГ ДАЛЬНОБОЙНОСТИ И ЧУВСТВИТЕЛЬНОСТИ):
            // * smoothstep(0.0, 2.5...) — это радиус действия взрыва через весь экран. 
            //   Если уменьшить 2.5 (например, до 0.8) — волна станет короткой. Буквы почувствуют свайп 
            //   только в упор к пальцу, а тапы далеко в Бездне их не побеспокоят.
            //   Если увеличить 2.5 (например, до 4.0) — радар накроет весь холст. Волна от любого тапа 
            //   в самом дальнем углу экрана мгновенно долетит до букв и сочно их качнёт.
            // * u_speed — это живой ток скорости. Сюда можно дописать множитель (например, u_speed * 2.0),
            //   чтобы сделать песок сверхувствительным даже к медленным, ленивым движениям пальца.
            float waveForce = (1.0 - smoothstep(0.0, 0.3, waveDist)) * u_speed;

            // 4. КИНЕТИЧЕСКОЕ ВЫТАКЛКИВАНИЕ (ВСТАВКА): Прибавляем вектор отскока к ndcPos.
            // * ЕСЛИ УМЕНЬШИТЬ МНОЖИТЕЛЬ (например, вместо 0.55 поставить 0.15) — слово станет 
            //   тяжёлым, жёстким и будет лишь слегка, благородно вздрагивать от свайпов в Бездне.
            // * ЕСЛИ УВЕЛИЧИТЬ МНОЖИТЕЛЬ (например, до 1.50) — песок станет невесомым и будет 
            //   сочно разлетаться шлейфом через весь экран от любого взмаха руки!
            ndcPos += normalize(waveDir) * waveForce * 0.10;

            
           
           

           gl_Position = vec4(ndcPos.x, -ndcPos.y, 0.0, 1.0);

            
            // Подключаем автоматический калибр размера точек из Resolution.js
            gl_PointSize = 0.5 * u_dpr;
        }
    `,

        

    // ФРАГМЕНТНЫЙ ШЕЙДЕР: Священное спасение воздуха + Атомарное дыхание золота по ID
    fragment: `
        precision highp float;

        uniform float u_time; // Провод покадровых часов из Uniforms.js
        varying float v_id;    // Приняли уникальный ID песчинки со сквозного кабеля

        void main() {
            // --- ТОТАЛЬНОЕ СПАСЕНИЕ ВОЗДУХА (ЗАКОН!) ---
            // Формула держит 50% чистых зазоров, заставляя песчинки просматриваться отдельно!
            vec2 coord = floor(gl_FragCoord.xy);
            if (mod(coord.x + coord.y, 2.0) == 0.0) {
                discard;
            }

            // Любимый чистый цвет червонного матового золота
            vec3 base_gold = vec3(0.85, 0.65, 0.08); 

            // --- МАТЕМАТИКА АТОМАРНОГО ПУЛЬСА ЯРКОСТИ ПО ID ---
            float wave = sin(u_time * 1.2 + v_id * 0.4);
            float brightness = mix(0.15, 1.0, wave * 0.5 + 0.5);

            // Умножаем матовое золото на вычисленный индивидуальный пульс яркости
            vec3 final_gold = base_gold * brightness;

            // Выводим готовое живое золото сквозь нерушимую сетку воздуха
            gl_FragColor = vec4(final_gold, 1.0);
        }
    `
};

// Замер скорости сборки чертежа и запечатывание памяти
const t0_sh = performance.now(); 
window.ShadersSource.duration = (performance.now() - t0_sh).toFixed(4);
Object.seal(window.ShadersSource);
